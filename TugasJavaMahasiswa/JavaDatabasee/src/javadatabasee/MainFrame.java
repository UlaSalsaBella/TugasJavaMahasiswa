    private void loadData() {
        //select * from mahasiswa    // Hapus dulu semua data di tabel GUI sebelum memuat ulang
        modelMahasiswa.setRowCount(0);

        try {
            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);
            stmt = conn.createStatement();

            // Query ambil semua data
            String sql = "SELECT \"NIM\", \"Nama\", \"TahunMasuk\" FROM public.\"Mahasiswaaa\"";
            ResultSet rs = stmt.executeQuery(sql);

            // Loop hasil query dan masukkan ke model tabel
            while (rs.next()) {
                String nim = rs.getString("NIM");
                String nama = rs.getString("Nama");
                int tahunMasuk = rs.getInt("TahunMasuk");

                Object[] data = {nama, nim, tahunMasuk};
                modelMahasiswa.addRow(data);
            }

            rs.close();
            stmt.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
            e.printStackTrace();
        }

    }

