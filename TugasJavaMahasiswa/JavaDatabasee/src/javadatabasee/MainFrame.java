    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String nim = txtNim.getText().trim();
        String nama = txtNama.getText().trim();
        int angkatan = 2025;

        if (nim.isEmpty() || nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "NIM dan Nama harus diisi!");
            return;
        }

        try {
            // Pastikan driver tersedia
            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);

            if (isEdit) {
                String sql = "UPDATE public.\"Mahasiswaaa\" SET \"Nama\" = ?, \"TahunMasuk\" = ? WHERE \"NIM\" = ?";
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(3, nim);
                pstmt.setString(1, nama);
                pstmt.setInt(2, angkatan);
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
                isEdit = false;
            } else {
                String sql = "INSERT INTO public.\"Mahasiswaaa\" (\"NIM\", \"Nama\", \"TahunMasuk\") VALUES (?, ?, ?)";
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, nim);
                pstmt.setString(2, nama);
                pstmt.setInt(3, angkatan);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            }

            // tampilkan ke tabel GUI
            Object[] dataBaru = {nama, nim, angkatan};
            modelMahasiswa.addRow(dataBaru);

            txtNim.setText("");
            txtNama.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tambahkan ini biar tahu detail error di console
        } finally {
            try {
                if (pstmt != null) {
                    pstmt.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = tblMahasiswa.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu!");
            return;
        }

        // Ambil data dari tabel
        String nim = (String) tblMahasiswa.getValueAt(selectedRow, 1);

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Apakah kamu yakin ingin menghapus data dengan NIM: " + nim + "?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            // Koneksi ke database
            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);

            // SQL DELETE
            String sql = "DELETE FROM public.\"Mahasiswaaa\" WHERE \"NIM\" = ?";
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, nim);

            int deleted = pstmt.executeUpdate();

            if (deleted > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus dari database!");
                // Hapus juga dari tabel GUI
                modelMahasiswa.removeRow(selectedRow);
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus! NIM tidak ditemukan di database.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (pstmt != null) {
                    pstmt.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnHapusActionPerformed

